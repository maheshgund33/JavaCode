//if Number is even print reverse and if number is odd then order reverse difference is 2
class program7{
        
        public static void main(String args[]){
                int N=15;
                if(N % 2==0){
			for(int i=N;i>0;i--){
				System.out.println(N);
			
			}
		}else{
			for(int i=N;i>0;i=i-2){
				System.out.println(N);
			}
		}
        }
}       

