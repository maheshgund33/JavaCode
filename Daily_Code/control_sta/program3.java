/*  -Take two integer A & B as input
 *  -print the max of two
 */

class If_Else_Ladder{

	public static void main(String args[]){
		int x=5;
		int y=5;

		if(x>y){
			System.out.println(x+"is greater");
		}else if(x<y){
			System.out.println(y+"is greater");
		}else{
			System.out.println("Both are Equal");
		}

	}
}
